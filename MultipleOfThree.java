/**
 * Print the sum and product of the first five integers
 * that are multiple of three.
 *
 * @author Kevin Lo 
 * @version 2020-08-25
 */
public class MultipleOfThree
{
    public static void main(String[] args)
    {
        System.out.print("The sum of 3 + 6 + 9 + 12 + 15 is ");
        System.out.print(3 + 6 + 9 + 12 + 15);
        System.out.println(".");
        System.out.println("");
        System.out.print("The product of 3 * 6 * 9 * 12 * 15 is ");
        System.out.print(3 * 6 * 9 * 12 * 15);
        System.out.println(".");


    }
}